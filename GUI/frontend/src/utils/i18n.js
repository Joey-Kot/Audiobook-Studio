const storageKey = "audiobook-studio-language";
const supportedLanguages = new Set(Object.keys(messages));
let lang = loadLanguage();

export function t(key) {
  return messages[lang][key] || messages.en[key] || key;
}

export function getLanguage() {
  return lang;
}

export function setLanguage(nextLanguage) {
  lang = supportedLanguages.has(nextLanguage) ? nextLanguage : "en";
  localStorage.setItem(storageKey, lang);
  return lang;
}

function loadLanguage() {
  const stored = localStorage.getItem(storageKey);
  if (supportedLanguages.has(stored)) {
    return stored;
  }
  return "en";
}
